const SORTED_READING_ENTRIES = [...BEGINNER_READING_ENTRIES].sort((a, b) => b[0].length - a[0].length)
const KANJI_RE = /[\u3400-\u9fff]/

function alreadyAnnotated(text: string, index: number, term: string) {
  const following = text.slice(index + term.length, index + term.length + 6)
  if (/^[\u3040-\u30ff\u3400-\u9fff々ヶ]*\(/.test(following)) return true

  const preceding = text.slice(Math.max(0, index - 8), index)
  return /\([ぁ-んァ-ンa-zA-Z]*$/.test(preceding)
}

export function isBeginnerLevel(level: string | null | undefined) {
  return level === 'A1' || level === 'A2'
}

export function annotateBeginnerFurigana(text: string) {
  if (!KANJI_RE.test(text)) return text

  let annotated = text
  for (const [term, reading] of SORTED_READING_ENTRIES) {
    annotated = annotated.replaceAll(term, (match, offset) => {
      if (alreadyAnnotated(annotated, offset, match)) return match
      return `${match}(${reading})`
    })
  }
  return annotated
}
